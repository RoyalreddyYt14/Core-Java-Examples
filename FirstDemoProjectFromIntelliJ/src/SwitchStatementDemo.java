public class SwitchStatementDemo {

    public static void main(String[] args) {
        int number = 0;
        switch (number){
            case 90:
                System.out.println("the number is "+number);
                break;
            case 1:
                System.out.println("the number is "+number);
                break;
            default:
                System.out.println("the default number is "+number);
                break;

        }
    }
}
