public class SwitchConDemo {

	public static void main(String[] args) {

		String beYear = "Five";
		switch (beYear) {
		case "First":
			System.out.println("Perla Reddy is first student");
			break;
		case "Second":
			System.out.println("Perla Reddy is second student");
			break;
		case "Third":
			System.out.println("Perla Reddy is third student");
			break;
		case "Fourth":
			System.out.println("Perla Reddy is fourth student");
			break;
		default:
			System.out.println("default");
			break;
		}
	}
}
